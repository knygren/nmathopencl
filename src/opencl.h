/**
 * @file opencl.h
 * @brief GLM‑specific OpenCL execution layer for glmbayes, providing GPU
 *        runners and high‑level Rcpp wrappers for f2/f3 log‑posterior and
 *        gradient evaluation.
 *
 * @namespace glmbayes::opencl
 * @brief Interfaces that connect glmbayes’ statistical kernels to their
 *        OpenCL‑ported implementations. This layer is distinct from
 *        openclPort (a general‑purpose OpenCL utility library intended
 *        for standalone use).
 *
 * @section Context
 *   The functions declared here are GLM‑specific and rely on OpenCL kernels
 *   that implement:
 *     - f2 (negative log‑posterior + prior component)
 *     - f3 (gradient of f2 with respect to β)
 *   for models whose parameter space has \(3^\ell\) faces. These kernels are
 *   typically generated by porting C++ routines (including portions of the
 *   nmath library and glmbayes’ own f2/f3 code) to OpenCL C.
 *
 *   Unlike openclPort—which provides general utilities for kernel loading,
 *   device enumeration, and Rcpp → std::vector conversion—this namespace
 *   provides the GLM‑aware execution path used internally by glmbayes.
 *
 * @section ImplementedIn
 *   These declarations are implemented in:
 *     - f2_f3_kernel_runner.cpp
 *     - f2_f3_opencl.cpp
 *
 * @section UsedBy
 *   These functions are consumed by:
 *     - EnvelopeBuild and EnvelopeDispersionBuild when GPU evaluation is enabled
 *     - rNormalGLM, rIndepNormalGammaReg, and related simulation routines
 *     - R wrappers that expose GPU‑accelerated f2/f3 evaluation to users
 *
 * @section Responsibilities
 *   Provides:
 *     - a low‑level GPU runner (f2_f3_kernel_runner) that executes the
 *       OpenCL kernel for f2/f3 evaluation on flattened buffers,
 *     - a high‑level Rcpp wrapper (f2_f3_opencl) that:
 *         • loads kernel sources,
 *         • flattens R matrices/vectors,
 *         • dispatches family/link‑specific kernels,
 *         • reconstructs outputs for R.
 *
 *   This module:
 *     - assumes validated inputs from R wrappers,
 *     - isolates GLM‑specific OpenCL logic from general OpenCL utilities,
 *     - enables optional GPU acceleration without altering CPU behavior.
 */


